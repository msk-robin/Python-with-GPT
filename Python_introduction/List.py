#List Manipulation

numbers = [1, 2, 3, 4, 5]

#Print the length of the list.
print(len(numbers))

# Print the first 3 elements of the list.
print(numbers[0:3])

#Append the number 6 to the end of the list and print the modified list.
numbers.append(6)
print(numbers)

#Remove the element at index 2 and print the modified list.
numbers.pop(2)
print(numbers)

