#String Manipulation

text = "Python is an amazing programming language"

#Print the length of the string.
print(len(text))

#Print the first 5 characters of the string.
print(text[0:5])

#Print the last 10 characters of the string.
print(text[31:])

#Print the string in uppercase.
print(text.capitalize())

# Replace "Python" with "Java" in the string and print the modified string.
print(text.replace("Python","Java"))

#Boolean Operations
is_student = True
is_adult = False

#Print the result of not is_student.
print(is_adult)

#Print the result of is_student and not is_adult.
print(is_student and not is_adult)

#Print the result of is_student or is_adult
print(is_student or is_adult)

#Print the result of is_student xor is_adult
print(is_student != is_adult)


    
    
    